    public boolean LargoPassword(String password)
    {
        final int LARGO_MINIMO_PASSWORD = 6;
        
        if( password.length() >= LARGO_MINIMO_PASSWORD ) // validacion del tamaño del password
        {
            return true;
            
        }else {
            
            JOptionPane.showMessageDialog(rootPane, "¡Password debe tener al menos " + LARGO_MINIMO_PASSWORD + " caracteres!");
            return false;
        }
    }
    
    public boolean CantidadMinusculasPassword( String password )
    {
        final int CANTIDAD_MINUSCULAS   = 1;
         
        int minusculas = 0;
   
        for( int i = 0; i < password.length(); i++ )
        {
            if( Character.isLowerCase(password.charAt(i) ) == true )
            {
                minusculas++;
            }
        }

        // validacion de las mayusculas
        if( minusculas >= CANTIDAD_MINUSCULAS ) 
        {

            return true;

        }else {

            JOptionPane.showMessageDialog(this, "¡Password debe  tener al menos " + CANTIDAD_MINUSCULAS + " minuscula(s)! ");
            return false;
        }        
    }

    public boolean CantidadMayusculasPassword( String password )
    {
        final int CANTIDAD_MAYUSCULAS   = 1;
         
        int mayusculas = 0;
   
        for( int i = 0; i < password.length(); i++ )
        {
            if( Character.isUpperCase( password.charAt(i) ) == true )
            {
                mayusculas++;
            }
        }

        // validacion de las mayusculas
        if( mayusculas >= CANTIDAD_MAYUSCULAS ) 
        {

            return true;

        }else {

            JOptionPane.showMessageDialog(this, "¡Password debe  tener al menos " + CANTIDAD_MAYUSCULAS + " mayuscula(s)! ");
            return false;
        }        
    }
    public boolean CantidadNumerosPassword( String password )
    {
        final int CANTIDAD_NUMEROS   = 1;
         
        int numeros = 0;
   
        for( int i = 0; i < password.length(); i++ )
        {
            if( Character.isDigit(password.charAt(i) ) == true )
            {
                numeros++;
            }
        }

        // validacion de los numeros
        if( numeros >= CANTIDAD_NUMEROS ) 
        {

            return true;

        }else {

            JOptionPane.showMessageDialog(this, "¡Password debe  tener al menos " + CANTIDAD_NUMEROS + " numero(s)! ");
            return false;
        }        
    }

