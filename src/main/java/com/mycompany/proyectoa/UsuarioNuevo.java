    private void jBottonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBottonCrearActionPerformed
        Usuario u = new Usuario();
        
        if( jTextfieldNombre.getText().isEmpty() == false )
        {
            u.nombre = jTextfieldNombre.getText();
        }else{
            JOptionPane.showMessageDialog(this, "Nombre vacio!!!");
            return;
        }
        
        if( jTextfieldPassword.getText().isEmpty() == false ) {
            
            u.password = jTextfieldPassword.getText();

        }else {
            JOptionPane.showMessageDialog(this, "Password vacio!!!");
            return;
        }
        
        if( jTextfieldUsuario.getText().isEmpty() == false) {
            
            u.usuario = jTextfieldUsuario.getText();

        }else {
            
            JOptionPane.showMessageDialog(this, "Usuario vacio!!!");
            return;
        }

        String rol = jComboBox1.getSelectedItem().toString();
        
        if(rol.equals("Administrador"))
        {
            u.rol = "A";
            
        }else {
            u.rol = "V";
        }
        
        u.estado = jComboBoxEstado.getSelectedItem().toString();
        
        if( LargoPassword(u.password) == true 
                && CantidadMayusculasPassword( u.password ) == true 
                && CantidadMinusculasPassword(u.password) == true  
                && CantidadNumerosPassword(u.password ) == true)
        {
        
            ProyectoFinal.usuarios.add(u);
        
            // Agregar funcion contraseña con una mayuscula y minimo 6 caracteres
        
            JOptionPane.showMessageDialog(this, "Usuario creado exitosamente");
        }
    }//GEN-LAST:event_jBottonCrearActionPerformed

