    private void jButtonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCrearActionPerformed
        Libro l = new Libro();
        
        if(jTextfieldTitulo.getText().isEmpty() == true ) {
            
            JOptionPane.showMessageDialog(this, "Debe de llenar campo titulo" );
            return;
            
        }else{
            
            l.titulo = jTextfieldTitulo.getText();

        }
        
        if(jTextfieldAutor.getText().isEmpty()== true) {
            
            JOptionPane.showMessageDialog(this, "Debe de llenar campo autor" );
            return;
            
        }else {
            
            l.autor = jTextfieldAutor.getText();
            
        }
        
        if(jTextfieldGenero.getText().isEmpty()== true) {
            
            JOptionPane.showMessageDialog(this, "Debe de llenar campo genero" );
            return;
            
        }else {
            
            l.genero = jTextfieldGenero.getText();
            
        }
        
        if(jTextfieldPrecio.getText().isEmpty()== true) {
            
            JOptionPane.showMessageDialog(this, "Debe de llenar campo precio" );
            return;
            
        }else {
            
            l.precio = Double.parseDouble(jTextfieldPrecio.getText());

        }
        
        if(jTextfieldStock.getText().isEmpty()== true) {
            
            JOptionPane.showMessageDialog(this, "Debe de llenar campo stock" );
            return;
            
        }else {
            
            l.cantidad_stock = Integer.parseInt(jTextfieldStock.getText());
            
        }
        
        ProyectoFinal.libros.add(l);
        JOptionPane.showMessageDialog(this, "Libro creado exitosamente");
        
    }//GEN-LAST:event_jButtonCrearActionPerformed

 

