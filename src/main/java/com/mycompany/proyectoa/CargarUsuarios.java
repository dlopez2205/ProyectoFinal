    private void pintarTabla() {
        
        String encabezado[] = {"Nombre","Password" , "Rol" , "Usuario" , "Estado"};
        DefaultTableModel t = new DefaultTableModel(encabezado, ProyectoFinal.usuarios.size());
        jTableCargarUsuarios.setModel(t);
        TableModel tabla = jTableCargarUsuarios.getModel();

        for(int i = 0; i < ProyectoFinal.usuarios.size();i++) {
            Usuario u = ProyectoFinal.usuarios.get(i);
            tabla.setValueAt(u.nombre, i, 0);
            tabla.setValueAt(u.password, i, 1);
            tabla.setValueAt(u.rol, i, 2);
            tabla.setValueAt(u.usuario, i, 3); 
            tabla.setValueAt(u.estado, i, 4); 

        }                
    } 
    
    public void leerXML(String path) {
        
        try {
            DocumentBuilderFactory constructor = DocumentBuilderFactory.newInstance();
            DocumentBuilder lector = constructor.newDocumentBuilder();
            
            Document document = lector.parse(new File(path));
            document.getDocumentElement().normalize();
            
            NodeList nodos = document.getElementsByTagName("usuario");
            
            for(int i = 0; i < nodos.getLength(); i ++)
            {
                Node nodo = nodos.item(i);
                
                if(nodo.getNodeType() == Node.ELEMENT_NODE)
                {
                    Usuario u = new Usuario();
                    
                    Element elemento = (Element) nodo;
                    u.nombre = elemento.getElementsByTagName("nombre").item(0).getTextContent();
                    u.password = elemento.getElementsByTagName("contraseÃ±a").item(0).getTextContent();
                    u.rol = elemento.getElementsByTagName("rol").item(0).getTextContent();
                    u.usuario = elemento.getElementsByTagName("idusuario").item(0).getTextContent();
                    
                    if(u.rol.equalsIgnoreCase("Administrador")) {
                        u.rol = "A";
                    }else {
                        u.rol = "V";
                    }
                    
                    u.estado = "Activo";
                    
                    ProyectoFinal.usuarios.add(u);
                    pintarTabla();                    
                }
            }
            
        } catch (Exception ex) {
            Logger.getLogger(CargarUsuarios.class.getName()).log(Level.SEVERE, null, ex);
        }
         
    }
    
