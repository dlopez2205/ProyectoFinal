    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
        if( cupon != null ){

            if( jTextFieldCodigoDescuento.getText().isEmpty() == false )
            {
                cupon.codigo_descuento = jTextFieldCodigoDescuento.getText();
            }else{
                JOptionPane.showMessageDialog(this, "Codigo de descuento vacio vacio!!!");
                return;
            }

            if( jTextFieldDescuento.getText().isEmpty() == false ) {

                cupon.descuento = Integer.parseInt(jTextFieldDescuento.getText());

            }else {
                JOptionPane.showMessageDialog(this, "Descuento vacio!!!");
                return;
            }

            cupon.tipo_descuento = jComboBoxTipoDescuento.getSelectedItem().toString();

            if( jTextFieldFechaVencimiento.getText().isEmpty()== false){

                String fecha = jTextFieldFechaVencimiento.getText();
                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                try {
                    cupon.fecha_de_vencimiento = formato.parse(fecha);
                } catch (ParseException ex) {
                    Logger.getLogger(ConsultaCupon.class.getName()).log(Level.SEVERE, null, ex);
                }

            }else {
                JOptionPane.showMessageDialog(this, "Fecha vacio!!!");
                return;
            }

            pintarTabla();
            JOptionPane.showMessageDialog(this, "Cupon modificado exitosamente");
            jTextFieldCodigoDescuento.setText("");
            jTextFieldDescuento.setText("");
            jComboBoxTipoDescuento.setSelectedIndex(-1);
            jTextFieldFechaVencimiento.setText("");   
        }
    }//GEN-LAST:event_jButtonGuardarActionPerformed

    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        jTableCupones.getSelectedRow();

        int eliminar = jTableCupones.getSelectedRow();

        if(eliminar <=  0 ){

            if(JOptionPane.showConfirmDialog(this, "Esta seguro de elimar")== 0)
            {
                ProyectoFinal.cupones.remove((eliminar));
                pintarTabla();

            }

        }else {

            JOptionPane.showMessageDialog(this, "Seleccione un cupon valido para elimirar");

        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        if(jTableCupones.getSelectedRow()>=0){

            cupon = ProyectoFinal.cupones.get(jTableCupones.getSelectedRow());
            jTextFieldCodigoDescuento.setText(cupon.codigo_descuento);
            jTextFieldDescuento.setText( Integer.toString(cupon.descuento));

            if(cupon.tipo_descuento.equals("Monto"))
            {
                jComboBoxTipoDescuento.setSelectedIndex(0);

            }else {

                jComboBoxTipoDescuento.setSelectedIndex(1);
            }
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            jTextFieldFechaVencimiento.setText( formato.format(cupon.fecha_de_vencimiento));

        }
    }//GEN-LAST:event_jButtonModificarActionPerformed

