    private void pintarTabla() {
        
        String encabezado[] = {"Codigo descuento","Descuento" , "Tipo descuento" , "Fecha de vencimiento"};
        DefaultTableModel t = new DefaultTableModel(encabezado, ProyectoFinal.cupones.size());
        jTableCupones.setModel(t);
        TableModel tabla = jTableCupones.getModel();

        for(int i = 0; i < ProyectoFinal.cupones.size();i++) {
            Cupon c = ProyectoFinal.cupones.get(i);
            tabla.setValueAt(c.codigo_descuento, i, 0);
            tabla.setValueAt(c.descuento, i, 1);
            tabla.setValueAt(c.tipo_descuento, i, 2);
            SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
            tabla.setValueAt(formato.format(c.fecha_de_vencimiento), i, 3);
        }                
    }

