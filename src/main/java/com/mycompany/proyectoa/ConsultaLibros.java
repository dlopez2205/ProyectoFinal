    private void jButtonBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBorrarActionPerformed
        jTableLibros.getSelectedRow();
        
        int eliminar = jTableLibros.getSelectedRow();
        
        if(eliminar >  0 ){
            
            if(JOptionPane.showConfirmDialog(this, "Esta seguro de elimar")== 0)
            {
                ProyectoFinal.libros.remove((eliminar));
                pintarTabla();
                
            }
            
        }else {
            
            JOptionPane.showMessageDialog(this, "Seleccione un libro valido para elimirar");
            
        }
    }//GEN-LAST:event_jButtonBorrarActionPerformed

    private void jButtonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonModificarActionPerformed
        if(jTableLibros.getSelectedRow()>=0){
           
           libro = ProyectoFinal.libros.get(jTableLibros.getSelectedRow());
           jTextFieldTitulo.setText(libro.titulo);
           jTextFieldAutor.setText(libro.autor);
           jTextFieldGenero.setText(libro.genero);
           jTextFieldPrecio.setText(Double.toString(libro.precio) );
           jTextFieldStock.setText(Integer.toString(libro.cantidad_stock));  
       }        
    }//GEN-LAST:event_jButtonModificarActionPerformed

