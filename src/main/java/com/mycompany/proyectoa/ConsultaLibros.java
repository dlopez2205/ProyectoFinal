    private void jButtonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGuardarActionPerformed
       
        if( libro != null ){

            if( jTextFieldTitulo.getText().isEmpty() == false )
            {
                libro.titulo = jTextFieldTitulo.getText();
            }else{
                JOptionPane.showMessageDialog(this, "Titulo vacio!!!");
                return;
            }

            if( jTextFieldAutor.getText().isEmpty() == false ) {

                libro.autor = jTextFieldAutor.getText();

            }else {
                JOptionPane.showMessageDialog(this, "Autor vacio!!!");
                return;
            }

            if( jTextFieldGenero.getText().isEmpty() == false) {

                libro.genero = jTextFieldGenero.getText();

            }else {

                JOptionPane.showMessageDialog(this, "Genero vacio!!!");
                return;
            }
            
            if( jTextFieldPrecio.getText().isEmpty()== false){
                
                libro.precio = Double.valueOf(jTextFieldPrecio.getText());
 
            }else {
                JOptionPane.showMessageDialog(this, "Precio vacio!!!");
                return;
            }
            
            if(jTextFieldStock.getText().isEmpty() == false) {
                libro.cantidad_stock = Integer.parseInt(jTextFieldStock.getText() );
            }
            
            pintarTabla();
            JOptionPane.showMessageDialog(this, "Libro modificado exitosamente");
            jTextFieldTitulo.setText("");
            jTextFieldAutor.setText("");
            jTextFieldGenero.setText("");
            jTextFieldPrecio.setText("");
            jTextFieldStock.setText("");
        }
        
    }//GEN-LAST:event_jButtonGuardarActionPerformed

